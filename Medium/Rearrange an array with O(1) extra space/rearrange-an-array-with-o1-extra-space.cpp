class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to rearrange an array so that arr[i] becomes arr[arr[i]]
    //with O(1) extra space.
    void arrange(long long a[], int n) {
        // Your code here
        
        for(long long i=0;i<n;i++){
            long long num1 = a[i];
            long long num2 = a[num1];
            
            a[i] = num1 + (num2%n)*n;
        }
        for(long long i=0;i<n;i++)
            a[i]/=n;
            
    }
};


